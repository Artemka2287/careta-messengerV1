package com.careta.common;

// Перечисление всех команд, которыми обмениваются клиент и сервер
public enum CommandType {
    // Авторизация
    LOGIN,          // Клиент отправляет логин/пароль
    REGISTER,       // Клиент отправляет данные для регистрации
    AUTH_SUCCESS,   // Сервер подтверждает вход
    AUTH_FAIL,      // Сервер отклоняет вход

    // Обмен сообщениями
    SEND_MESSAGE,        // Клиент отправляет сообщение
    RECEIVE_MESSAGE,     // Сервер пересылает сообщение клиенту
    GET_HISTORY,         // Клиент запрашивает историю чата
    HISTORY_RESPONSE,    // Сервер возвращает историю

    // Состояние сервера
    GET_ONLINE,      // Клиент запрашивает список подключённых
    ONLINE_UPDATE,   // Сервер рассылает обновлённый список

    // Поиск
    SEARCH_MESSAGES,    // Клиент ищет сообщения по ключевому слову
    SEARCH_RESPONSE     // Сервер возвращает найденные сообщения
}